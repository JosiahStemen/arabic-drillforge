def keywords(text):
    stop = {"the", "a", "an", "is", "are", "was", "were", "to", "in", "on", "at", "it", "i", "he", "she", "we", "they", "my", "his", "her", "me", "him"}
    words = re.findall(r"[a-z']+", text.lower())
    return [w for w in words if w not in stop and len(w) > 2]


def main():
    vocab = json.loads(Path("vocab.json").read_text(encoding="utf-8"))
    sentences = []
    for item in vocab["items"]:
        msa_ar = item["msa"].get("example", "").strip()
        lev_ar = item["lev"].get("example", "").strip()
        if not msa_ar and not lev_ar:
            continue
        trans = TRANSLATIONS.get(item["id"])
        if not trans:
            gloss = item["english"].split("/")[0].replace("to ", "").strip()
            trans = (f"Example with {gloss}.", f"Example with {gloss}.")
        msa_en, lev_en = trans
        if msa_ar:
            sentences.append({
                "id": f"{item['id']}-msa",
                "vocab_id": item["id"],
                "register": "msa",
                "arabic": msa_ar,
                "english": msa_en,
                "keywords": keywords(msa_en),
                "tags": item.get("tags", []),
            })
        if lev_ar:
            sentences.append({
                "id": f"{item['id']}-lev",
                "vocab_id": item["id"],
                "register": "lev",
                "arabic": lev_ar,
                "english": lev_en,
                "keywords": keywords(lev_en),
                "accept": [],
                "tags": item.get("tags", []),
            })
    out = {"_meta": {"description": "Arabic example sentences with English translations.", "count": len(sentences)}, "sentences": sentences}
    Path("sentences.json").write_text(json.dumps(out, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {len(sentences)} sentences")


if __name__ == "__main__":
    main()