def make_item(t):
    id_, typ, eng, msa_f, msa_t, root, msa_ex, lev_f, lev_t, lev_n, lev_ex, tags, dlpt, rank = t
    item = {
        "id": id_,
        "type": typ,
        "english": eng,
        "dlpt_level": dlpt,
        "msa": {
            "form": msa_f,
            "translit": msa_t,
            "example": msa_ex,
        },
        "lev": {
            "form": lev_f,
            "translit": lev_t,
            "notes": lev_n,
            "example": lev_ex,
        },
        "tags": tags,
        "frequency_rank": rank,
    }
    if root and root != "—":
        item["msa"]["root"] = root
    return item


def main():
    path = Path(__file__).with_name("vocab.json")
    data = json.loads(path.read_text(encoding="utf-8"))
    existing = {i["id"] for i in data["items"]}
    added = 0
    for t in NEW:
        if t[0] in existing:
            continue
        data["items"].append(make_item(t))
        added += 1
    verbs = sum(1 for i in data["items"] if i["type"] == "verb")
    nouns = sum(1 for i in data["items"] if i["type"] == "noun")
    phrases = sum(1 for i in data["items"] if i["type"] == "phrase")
    hp = sum(1 for i in data["items"] if "harry-potter" in (i.get("tags") or []))
    data["_meta"]["counts"] = {
        "verbs": verbs,
        "nouns": nouns,
        "phrases": phrases,
        "harry_potter": hp,
        "total": len(data["items"]),
    }
    data["_meta"]["description"] = (
        "Arabic DrillForge vocabulary. Types: verb, noun, phrase. "
        "Fields: id, type, english, msa/lev objects, tags, dlpt_level, frequency_rank. "
        "Tag harry-potter marks vocab for reading Harry Potter in Arabic."
    )
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Added {added} items. Total: {len(data['items'])} "
          f"(verbs {verbs}, nouns {nouns}, phrases {phrases}, harry-potter {hp})")


if __name__ == "__main__":
    main()
